// Return the system's CPU
Processor& System::Cpu() { return cpu_; }

// Return a container composed of the system's processes
vector<Process>& System::Processes() {
  // Processes panel will get cluttered by the same process after many times,
  // need to clear
  processes_.clear();

  vector<int> processList = LinuxParser::Pids();
  for (long unsigned int i = 0; i < processList.size(); i++) {
    // Do not need to call constructor if using emplace_back into vector or some
    // class thatinitialize with set of arguments
    processes_.emplace_back(processList[i]);
  }

  // Sort the processes based on cpu utilization with operator overloading
  // defined in process.cpp
  std::sort(processes_.begin(), processes_.end());
  return processes_;
}

// Return the system's kernel identifier (string)
std::string System::Kernel() { return LinuxParser::Kernel(); }

// Return the system's memory utilization
float System::MemoryUtilization() { return LinuxParser::MemoryUtilization(); }

// Return the operating system name
std::string System::OperatingSystem() { return LinuxParser::OperatingSystem(); }

// Return the number of processes actively running on the system
int System::RunningProcesses() { return LinuxParser::RunningProcesses(); }

// Return the total number of processes on the system
int System::TotalProcesses() { return LinuxParser::TotalProcesses(); }

// Return the number of seconds since the system started running
long int System::UpTime() { return LinuxParser::UpTime(); }
